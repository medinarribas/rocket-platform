namespace RocketPlatform {
    public class LandingArea {
        public int X { get; }
        public int Width { get; }
        public int Y { get; }
        public int Height { get; }

        public LandingArea(int x, int width, int y, int height) {
            X = x;
            Width = width;
            Y = y;
            Height = height;
        }
    }
}